#!/usr/bin/env node
/**
 * sync-audit-reports.mjs — surface the audit robots' reports inside the admin.
 *
 * IN PLAIN ENGLISH: your scheduled audit robots (SEO, AI features, Engagement,
 * Build-health) and the monthly master compiler write their findings to files
 * under docs/ — but the admin dashboard can't read loose Markdown at runtime.
 * This script mirrors those reports into ONE bundled data file the app imports,
 * so /admin/audits can show every audit's latest report + the consolidated
 * findings list. No LLM, no network — it just reads the files you already have.
 *
 * Reads:
 *   • docs/master-audit-report.json   (structured findings F-xx + sources + roadmap)
 *   • docs/audits/<category>/*.md      (per-audit dated reports → "last run" dates)
 *
 * Writes:
 *   • apps/web/src/data/audit-reports.json
 *
 * Honest by design: a missing report is simply absent (never invented). Pure
 * write of one JSON file. Never pushes.
 *
 * Usage: node scripts/sync-audit-reports.mjs [--quiet] [--print] [--no-write]
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const MASTER_FILE = path.join(ROOT, 'docs/master-audit-report.json');
const AUDITS_DIR = path.join(ROOT, 'docs/audits');
const OUT_FILE = path.join(ROOT, 'apps/web/src/data/audit-reports.json');

const args = process.argv.slice(2);
const QUIET = args.includes('--quiet');
const PRINT = args.includes('--print');
const NO_WRITE = args.includes('--no-write');

const log = (...a) => { if (!QUIET) console.log(...a); };
const warn = (...a) => console.warn(...a);

/** The audit-source directories under docs/audits we look for latest reports in. */
const REPORT_CATEGORIES = ['seo-aeo-geo', 'ai-features', 'build-health', 'engagement'];

const DATE_RE = /(\d{4}-\d{2}-\d{2})/;

/** Latest dated report (YYYY-MM-DD.md) in a category dir, or null. */
function latestReportFor(category) {
  const dir = path.join(AUDITS_DIR, category);
  if (!existsSync(dir)) return null;
  let names;
  try { names = readdirSync(dir); } catch { return null; }
  const reports = names
    .filter((n) => n.toLowerCase().endsWith('.md'))
    .map((n) => ({ name: n, date: (n.match(DATE_RE)?.[1]) ?? null }))
    .filter((r) => r.date)
    .sort((a, b) => b.date.localeCompare(a.date));
  if (reports.length === 0) {
    // No dated file — fall back to any .md so the owner still gets a link.
    const any = names.find((n) => n.toLowerCase().endsWith('.md'));
    return any ? { date: null, path: `docs/audits/${category}/${any}` } : null;
  }
  return { date: reports[0].date, path: `docs/audits/${category}/${reports[0].name}` };
}

/** Read + parse the master report JSON, or null when absent/corrupt. */
function readMaster() {
  if (!existsSync(MASTER_FILE)) return null;
  try {
    return JSON.parse(readFileSync(MASTER_FILE, 'utf8'));
  } catch (e) {
    warn(`sync-audit-reports: master report is unreadable (${e.message}).`);
    return null;
  }
}

function mapFinding(f) {
  return {
    id: String(f.id ?? ''),
    category: String(f.category ?? 'General'),
    finding: String(f.finding ?? ''),
    recommendation: String(f.recommendation ?? ''),
    priority: String(f.priority ?? '-'),
    effort: f.effort ? String(f.effort) : undefined,
    confidence: f.confidence ? String(f.confidence) : undefined,
    businessImpact: f.businessImpact ? String(f.businessImpact) : undefined,
    sources: Array.isArray(f.sources) ? f.sources.map(String) : [],
    status: String(f.status ?? 'open'),
  };
}

function mapSource(s) {
  return {
    id: String(s.id ?? ''),
    path: String(s.path ?? ''),
    type: String(s.type ?? ''),
    date: s.date ? String(s.date) : null,
    scheduled: Boolean(s.scheduled),
    schedule: s.schedule ? String(s.schedule) : undefined,
    contribution: s.contribution ? String(s.contribution) : undefined,
  };
}

function main() {
  const master = readMaster();

  const reports = {};
  for (const category of REPORT_CATEGORIES) {
    const latest = latestReportFor(category);
    if (latest) reports[category] = latest;
  }

  const snapshot = {
    generatedAt: new Date().toISOString(),
    note:
      'AUTO-GENERATED by scripts/sync-audit-reports.mjs — do not edit by hand. ' +
      'Mirrors docs/master-audit-report.json + docs/audits/* so /admin/audits can ' +
      'show audit reports in-app. Owner status tracking lives in audit-status-overrides.json.',
    masterGenerated: master ? String(master.generated ?? '') : '',
    masterStatus: master ? String(master.status ?? '') : '',
    findings: master && Array.isArray(master.consolidatedFindings)
      ? master.consolidatedFindings.map(mapFinding)
      : [],
    sources: master && Array.isArray(master.auditSources)
      ? master.auditSources.map(mapSource)
      : [],
    roadmap: master && master.roadmap && typeof master.roadmap === 'object' ? master.roadmap : {},
    reports,
  };

  if (PRINT) console.log(JSON.stringify(snapshot, null, 2));
  if (!NO_WRITE) writeFileSync(OUT_FILE, JSON.stringify(snapshot, null, 2) + '\n');

  log('');
  log(`✓ Audit Reports: ${snapshot.findings.length} finding(s), ` +
      `${snapshot.sources.length} source(s), ` +
      `${Object.keys(reports).length} per-audit report(s) → ${path.relative(ROOT, OUT_FILE)}`);
  if (!master) {
    log('  (No master report yet — the file is valid but empty. Run your audits to populate it.)');
  }
  log('  Review everything at /admin/audits.');
  log('');
}

export { latestReportFor, mapFinding, mapSource, REPORT_CATEGORIES };

const invokedDirectly = process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
if (invokedDirectly) {
  try { main(); } catch (e) { warn(`sync-audit-reports: ${e.message}`); process.exitCode = 1; }
}
