/**
 * Regenerates docs/INDEX.md — a self-updating table of contents for the docs/
 * folder. Scans every Markdown file, pulls its first H1 (or filename) as the
 * title and the first prose line as a one-line summary, and writes a grouped,
 * linked index. Run whenever docs are added/removed:  npm run docs:index
 *
 * Intentionally dependency-free and deterministic so it is safe to run anytime.
 */
import { readdirSync, statSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(HERE, '..');
const DOCS = path.join(REPO, 'docs');
const OUT = path.join(DOCS, 'INDEX.md');
const SELF = 'INDEX.md';

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const full = path.join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) out.push(...walk(full));
    else if (name.toLowerCase().endsWith('.md')) out.push(full);
  }
  return out;
}

function meta(file) {
  const lines = readFileSync(file, 'utf8').split(/\r?\n/);
  let title = null;
  let summary = '';
  for (let i = 0; i < lines.length; i++) {
    const l = lines[i].trim();
    if (!title) {
      const h1 = l.match(/^#\s+(.*)$/);
      if (h1) { title = h1[1].trim(); continue; }
    } else {
      // first non-empty, non-heading, non-blockquote line after the title
      if (l && !l.startsWith('#') && !l.startsWith('>') && !l.startsWith('<!--')) {
        summary = l.replace(/[*_`]/g, '').replace(/\[([^\]]+)\]\([^)]+\)/g, '$1');
        break;
      }
    }
  }
  if (!title) title = path.basename(file).replace(/\.md$/i, '');
  if (summary.length > 150) summary = summary.slice(0, 147).replace(/\s+\S*$/, '') + '…';
  return { title, summary };
}

const files = walk(DOCS)
  .filter((f) => path.relative(DOCS, f) !== SELF)
  .map((f) => ({ rel: path.relative(DOCS, f).split(path.sep).join('/'), ...meta(f) }));

// Group by top-level folder under docs/ ("" = docs root).
const groups = new Map();
for (const f of files) {
  const slash = f.rel.indexOf('/');
  const group = slash === -1 ? '' : f.rel.slice(0, slash);
  if (!groups.has(group)) groups.set(group, []);
  groups.get(group).push(f);
}

const order = [...groups.keys()].sort((a, b) => (a === '' ? -1 : b === '' ? 1 : a.localeCompare(b)));
const today = new Date().toISOString().slice(0, 10);

let md = '<!-- AUTO-GENERATED by scripts/build-docs-index.mjs — do not edit by hand. Run: npm run docs:index -->\n\n';
md += '# SwingVantage docs index\n\n';
md += `_${files.length} documents · regenerated ${today}. This file lists everything in \`docs/\` so it stays current as docs are added._\n`;

for (const g of order) {
  const items = groups.get(g).sort((a, b) => a.title.localeCompare(b.title));
  md += `\n## ${g === '' ? 'docs/' : 'docs/' + g + '/'}\n\n`;
  for (const it of items) {
    md += `- [${it.title}](${it.rel})` + (it.summary ? ` — ${it.summary}` : '') + '\n';
  }
}

writeFileSync(OUT, md);
console.log(`docs index written: ${path.relative(REPO, OUT)} (${files.length} docs)`);
