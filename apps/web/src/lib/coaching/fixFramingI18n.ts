/**
 * Resolve the full fix-framing string set for a language, with
 * English as the fallback for any missing language or key.
 */
export function resolveFixStrings(lang?: LanguageCode | null): FixStrings {
  if (!lang || lang === 'en') return FIX_STRINGS_EN;
  const overrides = FIX_I18N[lang];
  return overrides ? { ...FIX_STRINGS_EN, ...overrides } : FIX_STRINGS_EN;
}
